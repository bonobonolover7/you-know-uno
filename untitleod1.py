class Card:
    def __init__(self, color, value):
        self.color = color
        self.value = value

    def __str__(self):
        return f"{self.color} {self.value}"

    def __repr__(self):
        return f"Card('{self.color}', '{self.value}')"

    def is_action_card(self):
        return self.value in ['Skip', 'Reverse', 'Draw Two', 'Wild', 'Wild Draw Four']

class Deck:
    def __init__(self):
        self.cards = []
        colors = ['Red', 'Yellow', 'Green', 'Blue']
        values = [str(i) for i in range(10)] + ['Skip', 'Reverse', 'Draw Two']

        # Add colored cards
        for color in colors:
            for value in values:
                self.cards.append(Card(color, value)) # One of each for 0-9, Skip, Reverse, Draw Two
                if value != '0':
                    self.cards.append(Card(color, value)) # Two of each for 1-9, Skip, Reverse, Draw Two

        # Add Wild cards (4 of each)
        for _ in range(4):
            self.cards.append(Card('Wild', 'Wild'))
            self.cards.append(Card('Wild', 'Wild Draw Four'))

        self.shuffle()

    def shuffle(self):
        random.shuffle(self.cards)

    def draw_card(self):
        if not self.cards:
            print("Deck is empty, reshuffling discard pile into new deck.")
            # In a real game, you'd reshuffle the discard pile here.
            # For simplicity, we'll just create a new full deck if empty.
            self.__init__() # Re-initialize deck for demo purposes
            self.shuffle()
        return self.cards.pop()

    def __len__(self):
        return len(self.cards)

class Player:
    def __init__(self, name, is_bot=True):
        self.name = name
        self.hand = []
        self.is_bot = is_bot

    def add_card(self, card):
        self.hand.append(card)

    def remove_card(self, card_to_remove):
        for i, card in enumerate(self.hand):
            if card.color == card_to_remove.color and card.value == card_to_remove.value:
                return self.hand.pop(i)
        return None # Card not found

    def get_playable_cards(self, top_card, current_color_override=None):
        playable_cards = []
        for card in self.hand:
            # If Wild card was played, current_color_override dictates the playable color
            if current_color_override and card.color == current_color_override:
                playable_cards.append(card)
            elif card.color == 'Wild': # Wild cards can always be played
                playable_cards.append(card)
            elif card.color == top_card.color or card.value == top_card.value:
                playable_cards.append(card)

        return playable_cards

    def __str__(self):
        return f"{self.name} (봇: {self.is_bot})"

